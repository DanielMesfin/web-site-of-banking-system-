
public class Login {
	private String accountN;
	private String passwordN;
	
	public Login(String accountN, String passwordN) {
		this.accountN = accountN;
		this.passwordN = passwordN;
	}
	public String getAccountN() {
		return accountN;
	}
	public void setAccountN(String accountN) {
		this.accountN = accountN;
	}
	public String getPasswordN() {
		return passwordN;
	}
	public void setPasswordN(String passwordN) {
		this.passwordN = passwordN;
	}
	
}
